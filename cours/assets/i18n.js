window.AurelT = function(key) {
  const lang = localStorage.getItem('aurel.lang') || 'fr';
  return (window.AurelI18n[lang] && window.AurelI18n[lang][key]) || (window.AurelI18n.fr[key] || key);
};

window.AurelApplyI18n = function() {
  const lang = localStorage.getItem('aurel.lang') || 'fr';
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.innerHTML = window.AurelT(key);
  });
  document.querySelectorAll('[data-i18n-attr]').forEach(el => {
    // format: "attrName:key,attrName2:key2"
    const spec = el.getAttribute('data-i18n-attr');
    spec.split(',').forEach(pair => {
      const [attr, key] = pair.split(':').map(s => s.trim());
      if (attr && key) el.setAttribute(attr, window.AurelT(key));
    });
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
};
